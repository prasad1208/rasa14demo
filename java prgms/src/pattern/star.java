package pattern;

public class star 
{
    public static void main(String[] args) 
    {
		
	int n = 1;
		 
		for (int i=1; i<=4;i++)   //row
		{
			for(int j=1;j<=i;j++)   //column
				
			{
				System.out.println(i+" ");
				n++;
				}
			System.out.println();

     	}

    }
	
}
